'''oops ---class'''
class my_class:
    def add(a,b):
        return a+b
    def sub(a,b):
        return a-b
    def mul(a,b):
        return a*b
    def div(a,b):
        return a//b
    def mod(a,b):
        return a%b
obj1=my_class
obj2=my_class
print(obj1.add(2,3))
print(obj1.sub(2,3))
print(obj1.mul(2,3))
print(obj1.mod(2,3))
print(obj1.div(2,3))
print(obj2.add(5,7))
print(obj2.sub(5,7))
print(obj2.mul(5,7))
print(obj2.div(5,7))
print(obj2.mod(5,7))

